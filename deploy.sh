# MT5 Server Deployment Script
# This script helps deploy the MT5 server on a VPS

set -e

echo "ðŸš€ Starting MT5 Server Deployment"

# Check if running as root or with sudo
if [[ $EUID -eq 0 ]]; then
   echo "âŒ This script should not be run as root for security reasons"
   exit 1
fi

# Function to display colored output
print_status() {
    echo -e "\033[1;34m$1\033[0m"
}

print_success() {
    echo -e "\033[1;32mâœ… $1\033[0m"
}

print_error() {
    echo -e "\033[1;31mâŒ $1\033[0m"
}

print_warning() {
    echo -e "\033[1;33mâš ï¸  $1\033[0m"
}

# Check system requirements
print_status "Checking system requirements..."

# Check OS
if [[ "$OSTYPE" != "linux-gnu"* ]]; then
    print_error "This script is designed for Linux systems only"
    exit 1
fi

# Check architecture
ARCH=$(uname -m)
if [[ "$ARCH" != "x86_64" ]]; then
    print_error "This script requires x86_64 architecture"
    exit 1
fi

print_success "System requirements met"

# Install system dependencies
print_status "Installing system dependencies..."

sudo apt update
sudo apt install -y \
    python3 \
    python3-pip \
    python3-venv \
    curl \
    wget \
    gnupg2 \
    software-properties-common \
    ca-certificates \
    build-essential \
    libssl-dev \
    libffi-dev \
    python3-dev \
    git

print_success "System dependencies installed"

# Create application directory
APP_DIR="$HOME/mt5-server"
print_status "Setting up application directory: $APP_DIR"

if [[ -d "$APP_DIR" ]]; then
    print_warning "Directory $APP_DIR already exists. Backing up..."
    mv "$APP_DIR" "${APP_DIR}.backup.$(date +%Y%m%d_%H%M%S)"
fi

mkdir -p "$APP_DIR"
cd "$APP_DIR"

print_success "Application directory created"

# Copy application files (assuming they're in the current directory)
print_status "Copying application files..."

# Copy all necessary files
