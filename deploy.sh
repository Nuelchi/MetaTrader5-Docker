EOF

print_success "Systemd service created"

# Create log directory
print_status "Setting up logging..."

sudo mkdir -p /var/log
sudo touch /var/log/mt5-server.log
sudo chown $USER:$USER /var/log/mt5-server.log

print_success "Logging configured"

# Set up log rotation
print_status "Setting up log rotation..."

sudo tee /etc/logrotate.d/mt5-server > /dev/null << EOF
/var/log/mt5-server.log {
    daily
    missingok
    rotate 7
    compress
    delaycompress
    notifempty
    create 644 $USER $USER
    postrotate
        systemctl reload mt5-server
    endscript
}
EOF

print_success "Log rotation configured"

# Create data directories
print_status "Creating data directories..."

